package org.abego.guitesting;


import org.abego.commons.timeout.TimeoutSupplier;
import org.abego.commons.timeout.Timeoutable;

import org.eclipse.jdt.annotation.Nullable;
import java.awt.Component;
import java.awt.KeyboardFocusManager;

public interface FocusSupport extends TimeoutSupplier {
    /**
     * Return the focus owner, i.e. the {@link Component} that will receive all
     * KeyEvents generated by the user, or {@code null} if no focus owner is
     * defined
     * <p>
     * For details see {@link KeyboardFocusManager#getFocusOwner()}.
     */
    @Nullable
    Component focusOwner();

    /**
     * Wait until any component has the focus.
     */
    @Timeoutable
    void waitUntilAnyFocus();

    /**
     * Wait until {@code component} has the focus.
     */
    @Timeoutable
    void waitUntilInFocus(Component component);

    /**
     * Make the {@code component} have the focus.
     */
    @Timeoutable
    void setFocusOwner(Component component);

    /**
     * Move the focus to the next focusable component.
     * <p>
     * Return when the focus switched to a different component.
     * <p>
     * Important: as the function only returns when the focus has changed make
     * sure there is a "next" component to switch to (that is different from the
     * current focus owner).
     */
    @Timeoutable
    void focusNext();

    /**
     * Move the focus to the previous focusable component.
     * <p>
     * Returns when the focus switched to a different component.
     * <p>
     * Important: as the function only returns when the focus has changed make
     * sure there is a "previous" component to switch to, that is different from
     * the current focus owner.
     */
    @Timeoutable
    void focusPrevious();

    /**
     * Set the focus to the {@code component} and run the {@code runnable}.
     */
    @Timeoutable
    default void runWithFocusIn(Component component, Runnable runnable) {
        setFocusOwner(component);
        runnable.run();
    }

}
